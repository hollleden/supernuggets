'use client'

import { cn } from '@/lib/utils'

type Px = [number, number]
interface Props { className?: string; size?: number }

function Icon({ px, size = 14, className }: { px: Px[] } & Props) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 12 12"
      fill="currentColor"
      xmlns="http://www.w3.org/2000/svg"
      className={cn('shrink-0', className)}
      aria-hidden="true"
      style={{ imageRendering: 'pixelated' }}
    >
      {px.map(([x, y], i) => <rect key={i} x={x} y={y} width="1" height="1" />)}
    </svg>
  )
}

